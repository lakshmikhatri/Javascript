/* Function Arguments */
//function with two parameters or arguments
function demo(a,b){
    //a and b is formal arguments
    //when they receive 5and 9 value they become actual arguments
    //now  a=5 and b=9  
     return (a+b)/2;//return average of the two numbers
}
console.log(demo(5,9));

/* using Variables*/
let a=5,b=9;
console.log(demo(a,b)); //call function //pass variable as a value